import Ember from 'ember';
import RespondsToEscKeydown from '../mixins/responds-to-esc-keydown';

export default Ember.Component.extend(
  RespondsToEscKeydown,
{

  classNameBindings: [ 'active' ],
  attributeBindings:[ 'data-select-dropdown' ],

  active: false,
  'data-select-dropdown' : 1,

  trackedActions: {
    select: true // noop generated by GTM service
  },

  click: function (e) {
    var value       = $(e.target).attr('value');
    var isSelection = $(e.target).attr('data-selection');
    var isOverlay   = $(e.target).hasClass('select-dropdown-overlay');

    if (isOverlay) {
      return this.dismiss();
    }

    if (value && !isSelection) {
      this.send('select');
      this.set('selection', value);
    }

    this.toggleProperty('active');
  },

  selectedOption: function () {
    var selection = this.get('selection');
    return this.get('options').filter(function (option) {
      return (option.value || '').toString() === (selection || '').toString(); // N.B. html element attribute values are strings
    })[0];
  }.property('selection', 'options'),

  didInsertElement: function () {
    this.bindClickAway();
  },

  willDestroyElement: function () {
    this.unbindClickAway();
  },

  setListHeight: function () {
    if (!this.$()) return;
    var itemHeight = this.$().find('[data-option]').first().outerHeight();
    var length = this.get('active') ? this.get('options.length') + 1 : 1;
    this.$().find('[data-option-list]').css('height', itemHeight * length + 'px');
  }.observes('active').on('didInsertElement'),

  highlightSelected: function () {
    if (!this.$()) return;
    this.$().find('[value="' + this.get('selection') + '"]')
    .attr('selected', 'selected')
    .siblings().removeAttr('selected');
  }.observes('selection').on('didInsertElement'),

  bindClickAway: function () {
    // Select Dropdown has 2 types of 'click-away' dismissal.
    // One bound to the body, and one bound to a dismissal overlay.
    this.clickAwayFn = (e) => {
      if (this.get('isDestroyed')) return;
      var isClickAway = $(e.target).parents('[data-select-dropdown]').length < 1;
      if (isClickAway) this.dismiss();
    };

    $('body').on('click', this.clickAwayFn);
  },

  unbindClickAway: function () {
    $('body').off('click', this.clickAwayFn);
  },

  dismiss: function () {
    this.set('active', false);
  },

  // @return {boolean} stopPropagation
  escKeydown: function () {
    if (this.get('active')) {
      this.dismiss();
      return true;
    }
  }

});
